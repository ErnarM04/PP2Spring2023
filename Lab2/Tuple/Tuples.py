thistuple = ("apple", "banana", "cherry")
print(thistuple)

#Duplicates
thistuple = ("apple", "banana", "cherry", "apple", "cherry")
print(thistuple)

#Length
thistuple = ("apple", "banana", "cherry")
print(len(thistuple))

#Create tuple with one word
thistuple = ("apple",)
print(type(thistuple))

#NOT a tuple
thistuple = ("apple")
print(type(thistuple))

